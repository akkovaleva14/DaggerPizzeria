/*
Ключевое различие с предыдущими примерами:

В Example_13 используется аннотация @BindsInstance, которая позволяет:

Внедрять зависимости напрямую в компонент во время его построения
Более простой способ передачи объектов в граф зависимостей
Не требует создания отдельного модуля для провайда зависимости
Сравнение с Example_12:

Example_12: Использует кастомный модуль с передачей объекта через конструктор
Example_13: Использует @BindsInstance для прямого внедрения объекта в компонент
Преимущества @BindsInstance:

Меньше бойлерплейт кода
Прямая инъекция зависимостей в компонент
Удобство при работе с объектами, которые нужно внедрить, но не хранить в модуле
 */